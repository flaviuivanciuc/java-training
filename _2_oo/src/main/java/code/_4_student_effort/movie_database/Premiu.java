package code._4_student_effort.movie_database;

public class Premiu {

    private String nume;
    private int an;

    public Premiu(String nume, int an) {
        this.nume = nume;
        this.an = an;
    }

    public String getNume() {
        return nume;
    }

    public int getAn() {
        return an;
    }
}
